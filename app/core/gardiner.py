"""Comprehensive Gardiner Sign List — transliteration mapping (1000+ signs).

Facade module: imports raw sign data from gardiner_data/ package (26 category
files generated by scripts/build_gardiner_data.py), builds GardinerSign objects,
and exposes the same public API used by write.py, dictionary.py, and
transliteration.py.

Sources:
- Unicode 15.0 Egyptian Hieroglyphs block (U+13000–U+1342E)
- Gardiner, A.H. (1957) Egyptian Grammar, 3rd ed., Sign List pp.438–548
- Allen, J.P. (2014) Middle Egyptian, 3rd ed., pp.423–478
"""

from __future__ import annotations

import re
from dataclasses import dataclass
from enum import Enum

from app.core.gardiner_data import ALL_SIGNS, SIGN_INDEX


class SignType(Enum):
    UNILITERAL = "uniliteral"
    BILITERAL = "biliteral"
    TRILITERAL = "triliteral"
    LOGOGRAM = "logogram"
    DETERMINATIVE = "determinative"
    ABBREVIATION = "abbreviation"
    NUMBER = "number"


_TYPE_MAP: dict[str, SignType] = {t.value.upper(): t for t in SignType}


@dataclass
class GardinerSign:
    """Complete data for a single Gardiner sign."""
    code: str
    transliteration: str
    sign_type: SignType
    description: str
    category: str
    phonetic_value: str
    logographic_value: str = ""
    determinative_class: str = ""
    unicode_char: str = ""
    alt_transliterations: list[str] | None = None


def _get_category(code: str) -> str:
    """Extract category letter(s) from code: 'A5A'→'A', 'Aa15'→'Aa'."""
    if code.startswith("Aa"):
        return "Aa"
    m = re.match(r"([A-Z])", code)
    return m.group(1) if m else code[0]


