def _build_all_signs() -> dict[str, GardinerSign]:
    """Build GardinerSign objects from raw tuples in gardiner_data.

    Primary uniliterals are inserted first so that _build_reverse_map()
    in write.py encounters them before other signs that share the same
    phonetic value (e.g. S29 's' before O34 's').
    """
    # Primary uniliterals — these must appear first in iteration order
    _PRIMARY_UNILITERALS = [
        "G1", "M17", "M18", "D36", "G43", "D58", "Q3", "I9", "G17",
        "N35", "D21", "O4", "V28", "Aa1", "F32", "S29", "N37", "N29",
        "V31", "W11", "X1", "V13", "D46", "I10", "O34", "Z7",
    ]
    primary_set = set(_PRIMARY_UNILITERALS)

    def _make_sign(row: tuple) -> GardinerSign:
        code, hex_val, desc, phon, type_str, logo, det = row
        cat = _get_category(code)
        sign_type = _TYPE_MAP.get(type_str, SignType.DETERMINATIVE)
        return GardinerSign(
            code=code,
            transliteration=phon,
            sign_type=sign_type,
            description=desc,
            category=cat,
            phonetic_value=phon,
            logographic_value=logo or _LOGO_OVERLAYS.get(code, ""),
            determinative_class=det or _DET_OVERLAYS.get(code, ""),
            unicode_char=chr(hex_val),
            alt_transliterations=_ALT_TRANSLIT.get(code),
        )

    result: dict[str, GardinerSign] = {}

    # Pass 1: insert primary uniliterals first (iteration-order priority)
    for code in _PRIMARY_UNILITERALS:
        if code in SIGN_INDEX:
            result[code] = _make_sign(SIGN_INDEX[code])

    # Pass 2: insert all remaining signs
    for row in ALL_SIGNS:
        code = row[0]
        if code not in primary_set:
            result[code] = _make_sign(row)

    return result


GARDINER_TRANSLITERATION: dict[str, GardinerSign] = _build_all_signs()


# ═══════════════════════════════════════════════════════════
# Public helper API (backward-compatible)
# ═══════════════════════════════════════════════════════════

def get_transliteration(gardiner_code: str) -> str:
    """Get the transliteration for a Gardiner code."""
    sign = GARDINER_TRANSLITERATION.get(gardiner_code)
    if sign is None:
        return f"[{gardiner_code}]"
    if sign.phonetic_value:
        return sign.phonetic_value
    if sign.transliteration:
        return sign.transliteration
    return f"[{gardiner_code}]"


def get_sign_type(gardiner_code: str) -> SignType:
    """Get the type of a Gardiner sign."""
    sign = GARDINER_TRANSLITERATION.get(gardiner_code)
    if sign is None:
        return SignType.LOGOGRAM
    return sign.sign_type


def is_determinative(gardiner_code: str) -> bool:
    """Check if a Gardiner sign is primarily used as a determinative."""
    sign = GARDINER_TRANSLITERATION.get(gardiner_code)
    if sign is None:
        return False
    return sign.sign_type == SignType.DETERMINATIVE or bool(sign.determinative_class)


def get_determinative_class(gardiner_code: str) -> str:
    """Get the semantic class for a determinative sign."""
    sign = GARDINER_TRANSLITERATION.get(gardiner_code)
    if sign is None:
        return ""
    return sign.determinative_class


