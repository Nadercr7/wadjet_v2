"""
Wadjet AI — Rich Description Generation.

Generates dynamic, language-aware descriptions for Egyptian landmarks
using Gemini AI.  Results are cached for 1 hour to avoid redundant
API calls.

Phase 3.4 — Rich Description Generation.
"""

from __future__ import annotations

import json
from typing import Any

import structlog
from pydantic import BaseModel, Field

from app.core.cache import gemini_cache
from app.core.exceptions import GeminiError

logger = structlog.get_logger("wadjet.gemini.descriptions")

# ---------------------------------------------------------------------------
# Structured result model
# ---------------------------------------------------------------------------


class RichDescription(BaseModel):
    """Structured description generated by Gemini for a landmark."""

    landmark_name: str = Field(..., description="Name of the landmark")
    language: str = Field(..., description="Language code of the content")
    summary: str = Field(
        ...,
        description="One-paragraph overview of the landmark",
    )
    history: str = Field(
        ...,
        description="Historical background and significance",
    )
    architectural_significance: str = Field(
        ...,
        description="Architectural features and engineering importance",
    )
    interesting_facts: list[str] = Field(
        default_factory=list,
        description="4-6 interesting facts about the landmark",
    )
    visiting_tips: str = Field(
        ...,
        description="Practical tips for visitors",
    )
    best_time_to_visit: str = Field(
        default="",
        description="Recommended time of day / season to visit",
    )
    nearby_attractions: list[str] = Field(
        default_factory=list,
        description="Notable sites within walking distance",
    )


# ---------------------------------------------------------------------------
# JSON schema for Gemini structured output
# ---------------------------------------------------------------------------


def _description_response_schema() -> dict[str, Any]:
    """Return the JSON Schema for Gemini structured output."""
    return {
        "type": "object",
        "properties": {
            "summary": {"type": "string"},
            "history": {"type": "string"},
            "architectural_significance": {"type": "string"},
            "interesting_facts": {
                "type": "array",
                "items": {"type": "string"},
            },
            "visiting_tips": {"type": "string"},
            "best_time_to_visit": {"type": "string"},
            "nearby_attractions": {
                "type": "array",
                "items": {"type": "string"},
            },
        },
        "required": [
            "summary",
            "history",
            "architectural_significance",
            "interesting_facts",
            "visiting_tips",
            "best_time_to_visit",
            "nearby_attractions",
        ],
    }


