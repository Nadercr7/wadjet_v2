# ---------------------------------------------------------------------------
# Cache key helper
# ---------------------------------------------------------------------------


def _cache_key(landmark_name: str, language: str) -> str:
    """Build a deterministic cache key for a description."""
    return f"desc:{landmark_name.lower().strip()}:{language}"


# ---------------------------------------------------------------------------
# Core generation function
# ---------------------------------------------------------------------------


async def generate_description(
    gemini_service: Any,
    landmark_name: str,
    *,
    language: str = "en",
    use_cache: bool = True,
) -> RichDescription:
    """Generate a rich description for an Egyptian landmark.

    Parameters
    ----------
    gemini_service:
        An initialised ``GeminiService`` instance (from DI).
    landmark_name:
        The name of the landmark to describe.
    language:
        ISO 639-1 language code (``en``, ``ar``, ``fr``, ``de``).
    use_cache:
        If ``True`` (default), check/populate the 1-hour cache.

    Returns
    -------
    RichDescription
        Structured description with history, facts, tips, etc.

    Raises
    ------
    GeminiError
        If the Gemini API call fails or the response cannot be parsed.
    """
    cache_key = _cache_key(landmark_name, language)

    # ── Check cache ─────────────────────────────
    if use_cache:
        cached = gemini_cache.get(cache_key)
        if cached is not None:
            logger.info(
                "description_cache_hit",
                landmark=landmark_name,
                language=language,
            )
            return cached

    # ── Build prompt ────────────────────────────
    system_prompt = _SYSTEM_PROMPTS.get(language, _SYSTEM_PROMPTS["en"])

    user_prompt = (
        f"Generate a rich description for the Egyptian landmark: {landmark_name}\n\n"
        f"Return ONLY a valid JSON object following the specified schema."
    )

    logger.info(
        "description_generate_start",
        landmark=landmark_name,
        language=language,
    )

    # ── Call Gemini ──────────────────────────────
    try:
        raw_json = await gemini_service.generate_json(
            prompt=user_prompt,
            system_instruction=system_prompt,
            temperature=0.7,
            max_output_tokens=4096,
            response_schema=_description_response_schema(),
        )
    except GeminiError:
        raise
    except Exception as exc:
        logger.error("description_api_error", error=str(exc))
        raise GeminiError(f"Description generation failed: {exc}") from exc

    # ── Parse JSON ──────────────────────────────
    try:
        parsed: dict[str, Any] = json.loads(raw_json)
    except json.JSONDecodeError as exc:
        logger.error(
            "description_json_parse_error",
            raw_response=raw_json[:500],
            error=str(exc),
        )
        raise GeminiError(f"Gemini returned invalid JSON: {exc}") from exc

    # ── Validate with Pydantic ──────────────────
    try:
        result = RichDescription(
            landmark_name=landmark_name,
            language=language,
            **parsed,
        )
    except Exception as exc:
        logger.error(
            "description_validation_error",
            parsed_data=parsed,
            error=str(exc),
        )
        raise GeminiError(f"Description response failed validation: {exc}") from exc

    # ── Store in cache (1h TTL) ─────────────────
    if use_cache:
        gemini_cache.set(cache_key, result)

    logger.info(
        "description_generate_complete",
        landmark=landmark_name,
        language=language,
        facts_count=len(result.interesting_facts),
        nearby_count=len(result.nearby_attractions),
    )

    return result
